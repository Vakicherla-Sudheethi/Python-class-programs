#tuple creation
branch=("cse","it","ece","agri","mech","pt")
#slicing of tuples
print(branch)
#prints starting element
print(branch[1])
#prints n to n-1 ie 2 to 4
print(branch[2:5])
#gives 0-5
print(branch[:5])
#prints 3 to end
print(branch[3:])
#prints entire tuple
print(branch[:])
#starting to step count 2
print(branch[::2])
#having negative step count
print(branch[::-2])
#all elements from left to right
print(branch[::1])
#prints right to left in reverse order
print(branch[::-1])
#prints frm left to right in reverse order
print(branch[-5:-2])
